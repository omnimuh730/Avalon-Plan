import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const outPath = path.join(root, 'src/config/mongoSecrets.js');

// Cloud MongoDB — embedded at build/dev prep time, XOR-encoded in the output module.
// Non-SRV connection string (direct host seed list) so it works without a DNS
// SRV lookup, which some networks refuse (querySrv ECONNREFUSED).
// directConnection to one shard — multi-host replicaSet seed lists time out on Atlas shared tiers.
const MONGO_URL = 'mongodb://eatsy:Test.1234!@ac-iuyb1ae-shard-00-02.7n3pyfv.mongodb.net:27017/?ssl=true&authSource=admin&directConnection=true';
const MONGO_DB = 'AthensDB';
const XOR_KEY = 'foxhire-bridge-v1';

function xorBytes(value, key) {
  const bytes = [];
  for (let i = 0; i < value.length; i++) {
    bytes.push(value.charCodeAt(i) ^ key.charCodeAt(i % key.length));
  }
  return bytes;
}

function xorKeyBytes(key) {
  return [...key].map((ch) => ch.charCodeAt(0));
}

const urlBytes = xorBytes(MONGO_URL, XOR_KEY);
const dbBytes = xorBytes(MONGO_DB, XOR_KEY);
const keyBytes = xorKeyBytes(XOR_KEY);

const source = `// Auto-generated by scripts/generate-mongo-secrets.mjs — do not edit.
const _kb = [${keyBytes.join(', ')}];
const _ub = [${urlBytes.join(', ')}];
const _db = [${dbBytes.join(', ')}];

function _key() {
  return String.fromCharCode(..._kb);
}

function _dec(bytes) {
  const k = _key();
  return String.fromCharCode(...bytes.map((b, i) => b ^ k.charCodeAt(i % k.length)));
}

export function getMongoUrl() {
  return _dec(_ub);
}

export function getMongoDbName() {
  return _dec(_db);
}
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, source);
console.log('[mongo-secrets] Wrote', path.relative(root, outPath));
